/*
Operator Augmented Assigments: (mempersingkat operator aritmatika) 
    - bilangan += 10 (bilangan = bilangan + 10)
    - bilangan -= 10 (bilangan = bilangan + 10)
    - bilangan *= 10 (bilangan = bilangan * 10)
    - bilangan /= 10 (bilangan = bilangan / 10)
    - bilangan %= 10 (bilangan = bilangan % 10)
*/

// contoh kode operator assigments

//kode assignments penjumlahan
let bilangan = 10 + 8; // hasil 18
document.writeln(bilangan);

// kode dibawah ini baris baru
document.writeln("</br>");

//operator assignments pengurangan
bilangan = bilangan -= 10; // hasil 8
document.writeln(bilangan);

// kode dibawah ini baris baru
document.writeln("</br>");

//kode assignments perkalian
bilangan = bilangan *= 10; // hasil 80
document.writeln(bilangan);

// kode dibawah ini baris baru
document.writeln("</br>");

//operator assignments modulus atau sisa bagi 
perkalian = bilangan %= 10; // sisa dari hasil 80 dibagi 10 adalah 0 
document.writeln(perkalian);

