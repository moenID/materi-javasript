/*
Operator Unary
    - oerator + (menandakan nilai positif)
    - operator - (menandakan nilai negatif)
    - operator ++ (increment, atau menaikkan 1 angka)
    - operator -- (decrement, atau menurunkan 1 angka)
*/

// contoh kode operator unary

//membuat variable dengan nilai bahwa bilangan tersebut positif
let bilangan = +10
document.writeln(bilangan);

//kode ini untuk membuat baris baru
document.writeln("</br>")

//operator unary menaikkan 1 angka
bilangan++;
document.writeln(bilangan);

//kode ini untuk membuat baris baru
document.writeln("</br>")

//operator unary menurunkan satu angka
bilangan--;
document.writeln(bilangan);

//kode ini untuk membuat baris baru
document.writeln("</br>")

//operator unary menandakan nilai negatif
bilangan = -bilangan;
document.writeln(bilangan);