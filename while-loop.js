/*
while loop merupakan perulangan yang lebih sederhana dari pada for loop
di while loop, hanya terdapat perulangan, tanpa ada init statement dan post statement
*/

// contoh kode while loop
let bilangan = 1;
while (bilangan <= 10) {
    document.writeln(`<p>Perulangan ke ${bilangan}</p>`);
    bilangan++
};
